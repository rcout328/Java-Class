public class typecast {
    public static void main(String [] args){
        int a =10;
        double d = a;
        System.out.println("1"+a);
        System.out.println("1"+d);


        double b = 20.99;
        int c =(int)b;
        System.out.println("2"+b);
        System.out.println("2"+c);
    } 
}
